// ============
// HELPERS
// ============

String.prototype.format = function(){
	var string = this;

	for (var i = 0, j = arguments.length; i < j; i++) {
		string = string.replace(new RegExp('\\{' + i + '\\}', 'gm'), arguments[i]);
	}

	return string;
};

String.prototype.rexp = function(reg){
	return this.replace(reg, "<span class='highlight'>$&</span>");
};


// ============
// INTERFACE
// ============

function filter(what,r){
	tpl = "";
	for (var prop in table) {
		el = table[prop];
		if(r && r != ""){
			var reg = new RegExp(r, "gi");
			switch(what){
				case "s":
					if(el.s.toLowerCase().indexOf(r.toLowerCase()) > -1){ 
						tpl += li.format(el.c, el.s, prop, el.n, el.w); 
					}
					break;
				case "p":
					if(prop.toLowerCase().indexOf(r.toLowerCase()) > -1){
						tpl += li.format(el.c, el.s, prop.toString().rexp(reg), el.n, el.w);
					}
					break;
				case "n":
					if(el.n.toString().indexOf(r) > -1){
						tpl += li.format(el.c, el.s, prop, el.n.toString().rexp(reg), el.w);
					}
					break;
				case "w":
					if(el.w.toString().indexOf(r) > -1){ 
						tpl += li.format(el.c, el.s, prop, el.n, el.w.toString().rexp(reg));
					}
					break;
				default:
					break;
			}
		} else {
			tpl += li.format(el.c, el.s, prop, el.n, el.w);
		}	
	}
	pList.innerHTML = tpl;
}


